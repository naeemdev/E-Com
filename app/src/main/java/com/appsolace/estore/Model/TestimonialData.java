package com.appsolace.estore.Model;



public class TestimonialData {


    private String testimonial_id;
    private String name;
    private String description;
    private String image;

    public String getTestimonial_id() {
        return testimonial_id;
    }

    public void setTestimonial_id(String testimonial_id) {
        this.testimonial_id = testimonial_id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }


}
